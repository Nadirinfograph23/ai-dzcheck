// ==================== PWA ====================
var deferredPrompt = null;

function isMobileDevice() {
    return /Android|webOS|iPhone|iPad|iPod|BlackBerry|IEMobile|Opera Mini/i.test(navigator.userAgent) ||
           (window.innerWidth <= 768);
}

function initPWA() {
    window.addEventListener('beforeinstallprompt', function(e) {
        e.preventDefault();
        deferredPrompt = e;
        if (!isMobileDevice()) return;
        var banner = document.getElementById('pwaInstallBanner');
        if (banner) {
            banner.classList.add('show');
            document.body.classList.add('pwa-banner-visible');
        }
    });

    var installBtn = document.getElementById('pwaInstallBtn');
    if (installBtn) {
        installBtn.addEventListener('click', function() {
            if (deferredPrompt) {
                deferredPrompt.prompt();
                deferredPrompt.userChoice.then(function() {
                    deferredPrompt = null;
                    document.getElementById('pwaInstallBanner').classList.remove('show');
                    document.body.classList.remove('pwa-banner-visible');
                });
            }
        });
    }

    var closeBtn = document.getElementById('pwaCloseBtn');
    if (closeBtn) {
        closeBtn.addEventListener('click', function() {
            document.getElementById('pwaInstallBanner').classList.remove('show');
            document.body.classList.remove('pwa-banner-visible');
        });
    }

    // Register service worker
    if ('serviceWorker' in navigator) {
        navigator.serviceWorker.register('sw.js').catch(function(err) {
            console.log('SW registration failed:', err);
        });
    }
}

// ==================== EVENT LISTENERS ====================
document.addEventListener('DOMContentLoaded', function() {
    createParticles();
    setLanguage(currentLang);
    initPWA();

    // Language switcher
    var langBtn = document.getElementById('langBtn');
    var langDropdown = document.getElementById('langDropdown');
    
    langBtn.addEventListener('click', function(e) {
        e.stopPropagation();
        langDropdown.classList.toggle('show');
    });

    document.querySelectorAll('.lang-option').forEach(function(opt) {
        opt.addEventListener('click', function() {
            setLanguage(this.getAttribute('data-lang'));
            langDropdown.classList.remove('show');
        });
    });

    document.addEventListener('click', function() {
        langDropdown.classList.remove('show');
    });

    // File upload
    var uploadArea = document.getElementById('uploadArea');
    var fileInput = document.getElementById('fileInput');
    var uploadBtn = document.getElementById('uploadBtn');

    uploadBtn.addEventListener('click', function(e) {
        e.stopPropagation();
        fileInput.click();
    });

    uploadArea.addEventListener('click', function() {
        fileInput.click();
    });

    uploadArea.addEventListener('dragover', function(e) {
        e.preventDefault();
        uploadArea.classList.add('drag-over');
    });

    uploadArea.addEventListener('dragleave', function() {
        uploadArea.classList.remove('drag-over');
    });

    uploadArea.addEventListener('drop', function(e) {
        e.preventDefault();
        uploadArea.classList.remove('drag-over');
        var files = e.dataTransfer.files;
        if (files.length > 0) handleFile(files[0]);
    });

    fileInput.addEventListener('change', function() {
        if (fileInput.files.length > 0) {
            handleFile(fileInput.files[0]);
            fileInput.value = '';
        }
    });

    function handleFile(file) {
        // Validate file
        var maxSize = 50 * 1024 * 1024;
        if (file.size > maxSize) {
            showToast(t('toast_too_large'), 'error');
            return;
        }
        var validTypes = ['image/', 'video/', 'audio/'];
        var isValid = validTypes.some(function(type) { return file.type.startsWith(type); });
        if (!isValid) {
            showToast(t('toast_invalid'), 'error');
            return;
        }
        runAnalysis(file);
    }

    // Report buttons
    var downloadBtn = document.getElementById('downloadReport');
    if (downloadBtn) {
        downloadBtn.addEventListener('click', downloadReport);
    }
    
    var newBtn = document.getElementById('newAnalysis');
    if (newBtn) {
        newBtn.addEventListener('click', resetAnalysis);
    }

    // Smooth scroll for nav links
    document.querySelectorAll('.nav-link').forEach(function(link) {
        link.addEventListener('click', function(e) {
            var href = this.getAttribute('href');
            if (href.startsWith('#')) {
                e.preventDefault();
                var target = document.querySelector(href);
                if (target) {
                    target.scrollIntoView({ behavior: 'smooth', block: 'start' });
                }
            }
        });
    });
});
